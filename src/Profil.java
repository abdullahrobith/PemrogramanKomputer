    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int n = tabelproduk.getSelectedRow();  // Mendapatkan baris yang dipilih
if(n != -1){
    // Ambil ID produk yang dipilih
    int id = Integer.parseInt(tabelproduk.getValueAt(n, 0).toString());

    // Ambil data produk yang dipilih (seperti nama produk, kategori, dll.)
    String kodeProduk = tabelproduk.getValueAt(n, 1).toString();  // Misalnya, kode produk ada di kolom ke-2
    String namaProduk = tabelproduk.getValueAt(n, 2).toString();  // Misalnya, nama produk ada di kolom ke-3
    String gambarProduk = tabelproduk.getValueAt(n, 3).toString(); // Gambar produk di kolom ke-4

    // Ambil kategori dan supplier yang dipilih dari combo box
    String kategoriProdukString = tabelproduk.getValueAt(n, 4).toString(); // Kategori di kolom ke-5
    String supplierString = tabelproduk.getValueAt(n, 5).toString(); // Supplier di kolom ke-6

    // Ambil angka ID dari kategori dan supplier
    int kategoriProdukID = Integer.parseInt(kategoriProdukString.split("-")[0]);
    int supplierID = Integer.parseInt(supplierString.split("-")[0]);

    // Ambil harga jual, harga beli, dan stok produk
    double hargaJual = Double.parseDouble(tabelproduk.getValueAt(n, 6).toString());  // Harga jual di kolom ke-7
    double hargaBeli = Double.parseDouble(tabelproduk.getValueAt(n, 7).toString());  // Harga beli di kolom ke-8
    int stokProduk = Integer.parseInt(tabelproduk.getValueAt(n, 8).toString()); // Stok produk di kolom ke-9

    // Tampilkan form untuk mengubah data produk
    UbahProduk U = new UbahProduk(this, true);
    U.setId(id);
    U.setKodeProduk(kodeProduk);
    U.setNamaProduk(namaProduk);
    U.setGambarProduk(gambarProduk);
    U.setKategoriProdukID(kategoriProdukID);
    U.setSupplierID(supplierID);
    U.setHargaJual(hargaJual);
    U.setHargaBeli(hargaBeli);
    U.setStokProduk(stokProduk);
    U.setVisible(true); // Menampilkan form ubah data produk
}

        
    }//GEN-LAST:event_jButton4ActionPerformed

